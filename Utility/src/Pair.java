
public class Pair {
	private double first;
	private double second;
	
	public Pair(double a, double b) {
		first = a;
		second = b;
	}

	public double getFirst() {
		return first;
	}
	
	public double getSecond() {
		return second;
	}

	public void setFirst(double a) {
		this.first = a;
	}
	
	public void setSecond(double b) {
		this.second = b;
	}
}
